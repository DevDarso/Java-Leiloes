private void listarVendidos(){
    try {
        // Declarando apenas uma vez o modelo de tabela
        DefaultTableModel model = (DefaultTableModel) TableVendidos.getModel();
        model.setNumRows(0); // Limpa as linhas da tabela
        
        // Definindo as colunas (se necessário)
        if (model.getColumnCount() == 0) {
            model.setColumnIdentifiers(new String[] {"id", "nome", "valor", "status"});
        }

        // Obtendo os produtos
        ProdutosDAO produtosdao = new ProdutosDAO();
        ArrayList<ProdutosDTO> listagem = produtosdao.listarVendidos();
        
        // Adicionando os dados à tabela
        for (int i = 0; i < listagem.size(); i++) {
            model.addRow(new Object[] {
                listagem.get(i).getId(),
                listagem.get(i).getNome(),
                listagem.get(i).getValor(),
                listagem.get(i).getStatus()
            });
        }
    } catch (Exception e) {
        e.printStackTrace(); // Melhor incluir um print para depuração do erro
    }
}

